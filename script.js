/* ==========================================================
   EVENTOS DOS CARDS
========================================================== */

function adicionarEventosCards(){

    /* FOTO */

    document.querySelectorAll(".foto-produto").forEach(foto=>{

        foto.addEventListener("click",()=>{

            const produto = listaAtual[foto.dataset.index];

            abrirModal(produto);

        });

    });


    /* BOTÃO ADICIONAR */

    document.querySelectorAll(".btn-adicionar").forEach(botao=>{

        botao.addEventListener("click",()=>{

            const nome = botao.dataset.produto;

            if(!pedido[nome]){

                pedido[nome]={
                    quantidade:1
                };

            }else{

                pedido[nome].quantidade++;

            }

            salvarPedido();

            renderizarProdutos(obterListaFiltrada());

            atualizarCarrinho();

        });

    });


    /* BOTÃO + */

    document.querySelectorAll(".mais").forEach(botao=>{

        botao.addEventListener("click",()=>{

            const nome=botao.dataset.produto;

            if(!pedido[nome]){

                pedido[nome]={
                    quantidade:1
                };

            }else{

                pedido[nome].quantidade++;

            }

            salvarPedido();

            renderizarProdutos(obterListaFiltrada());

            atualizarCarrinho();

        });

    });
        /* BOTÃO - */

    document.querySelectorAll(".menos").forEach(botao=>{

        botao.addEventListener("click",()=>{

            const nome = botao.dataset.produto;

            if(!pedido[nome]) return;

            pedido[nome].quantidade--;

            if(pedido[nome].quantidade<=0){

                delete pedido[nome];

            }

            salvarPedido();

            renderizarProdutos(obterListaFiltrada());

            atualizarCarrinho();

        });

    });

}

